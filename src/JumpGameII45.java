    private static int jumpHelperBfs(int sourceIndex, int targetIndex, int[] nums) {
        Queue<Integer> queue = new LinkedList<>() ;
        queue.add(sourceIndex);
        Set<Integer> visitedIndices = new HashSet<>();
        visitedIndices.add(sourceIndex);
        int level=0;

        while(!queue.isEmpty()){
            int qSize =queue.size();
          //  System.out.println("level"+level);
           // System.out.println("queue"+queue);

            for (int i=0;i<qSize;i++){
            
                int currentIndex = queue.poll();    
               // visitedIndices.add(currentIndex)              
                if (currentIndex==targetIndex){
                    return level;
                }
                                      
              //  List<Integer> potentialN = new ArrayList<>();
                int maxPossibleJump = nums[currentIndex];
                if(maxPossibleJump==0){continue;}
                int target = Math.min(currentIndex+maxPossibleJump,nums.length-1);

                for (int k=currentIndex+1;k<=target;k++){

                    int nIndex= k;
                    if(!visitedIndices.contains(nIndex) ){
                      //  System.out.println("nIndex"+nIndex);
                      //  System.out.println("target"+target);
                      visitedIndices.add(nIndex);
                        queue.add(nIndex);
                        
                    }
                }

            }
            level++;

        }

        
        

       
           

return -1;
    }


